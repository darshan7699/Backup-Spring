package com.te.springcoreautowirejavabase;

public class Laptop {
 private String name;
 private double price;
 private String os;
 private Screen sc;
 
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public double getPrice() {
	return price;
}
public void setPrice(double price) {
	this.price = price;
}
public String getOs() {
	return os;
}
public void setOs(String os) {
	this.os = os;
}

public Screen getSc() {
	return sc;
}
public void setSc(Screen sc) {
	this.sc = sc;
}
@Override
public String toString() {
	return "Laptop [name=" + name + ", price=" + price + ", os=" + os + ", sc=" + sc + "]";
}

 
 
}
